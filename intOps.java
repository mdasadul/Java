public class intOps{
public static void main(String[] args){
int firstInput = Integer.parseInt(args[0]);
int secondInput = Integer.parseInt(args[1]);
int addNumber = firstInput + secondInput;
int subNumber = firstInput - secondInput;
System.out.println(firstInput +" + "+ secondInput+ " = "+ addNumber+ "\n");
System.out.println(firstInput +" - "+ secondInput+ " = " + subNumber);
}
}
